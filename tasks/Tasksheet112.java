package tasks;

public class Tasksheet112 {
    public static void main(String[] args) {
        int result = 1 + 2; // result is now 3

        System.out.println(result);

        result -= 1;                // result = 2
        System.out.println(result);

        result *= 2;                // result = 4
        System.out.println(result);

        result /= 2;                // result = 2
        System.out.println(result);

        result += 8;                // result = 10
        result %= 7;                // result = 3
        System.out.println(result);
    }
}

